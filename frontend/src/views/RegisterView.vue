<script>
export default {
	data() {
		return {
			form: {
				name: '',
				email: '',
				phone: '',
				password: '',
				confirmPassword: '',
				acceptTerms: false
			},
			showPassword: false,
			showConfirmPassword: false
		}
	},
	computed: {
		passwordStrength() {
			const password = this.form.password
			if (!password) return 0
			
			let strength = 0
			if (password.length >= 8) strength++
			if (password.match(/[a-z]/) && password.match(/[A-Z]/)) strength++
			if (password.match(/[0-9]/)) strength++
			if (password.match(/[^a-zA-Z0-9]/)) strength++
			
			return strength
		},
		isFormValid() {
			return (
				this.form.name &&
				this.form.email &&
				this.form.phone &&
				this.form.password.length >= 8 &&
				this.form.password === this.form.confirmPassword &&
				this.form.acceptTerms
			)
		}
	},
	mounted() {
  		this.$nextTick(() => {
    	window.scrollTo({ top: 0, behavior: 'smooth' })
  	})
	},
	methods: {
		handleRegister() {
			if (this.form.password !== this.form.confirmPassword) {
				alert('Heslá sa nezhodujú!')
				return
			}
			console.log('Register attempt:', this.form)
			// Tu pridáš logiku pre registráciu
		},
		getStrengthColor() {
			const colors = ['bg-red-500', 'bg-orange-500', 'bg-yellow-500', 'bg-green-500']
			return colors[this.passwordStrength - 1] || 'bg-gray-200'
		},
		getStrengthText() {
			const texts = ['Slabé heslo', 'Priemerné heslo', 'Dobré heslo', 'Silné heslo']
			return texts[this.passwordStrength - 1] || 'Zadajte heslo'
		},
		getStrengthTextColor() {
			const colors = ['text-red-600', 'text-orange-600', 'text-yellow-600', 'text-green-600']
			return colors[this.passwordStrength - 1] || 'text-gray-500'
		}
	}
}
